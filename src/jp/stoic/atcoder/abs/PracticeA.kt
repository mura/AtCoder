package jp.stoic.atcoder.abs

import java.util.*

fun main(args: Array<String>) {
    val sc = Scanner(System.`in`)
    val first: Int = sc.nextInt()
    val second: Int = sc.nextInt()
    val third: Int = sc.nextInt()
    val text: String = sc.next()
    println((first + second + third).toString() + " " + text)
}